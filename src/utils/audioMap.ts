/**
 * Получить числовой идентификатор аудио файла
 */
export const getAudioAssetId = (path: string): number | null => {
    if (!path || path.trim() === '') {
        console.log('❌ Путь к аудио пустой');
        return AUDIO_MAP['placeholder'];
    }

    console.log(`🔍 Ищем аудио для пути: "${path}"`);

    // Вариант 1: Прямой поиск (пути из БД без расширения)
    if (AUDIO_MAP[path]) {
        console.log(`✅ Найдено точное совпадение: "${path}"`);
        return AUDIO_MAP[path];
    }

    // Вариант 2: Убираем расширение если есть и ищем
    const pathWithoutExt = path.replace(/\.mp3$/i, '');
    if (pathWithoutExt !== path && AUDIO_MAP[pathWithoutExt]) {
        console.log(`✅ Найдено без расширения: "${path}" -> "${pathWithoutExt}"`);
        return AUDIO_MAP[pathWithoutExt];
    }

    // Вариант 3: Ищем частичное совпадение (на случай различий в регистре или разделителях)
    const normalizedSearchPath = path.toLowerCase().replace(/[\\\/]/g, '/');

    for (const key in AUDIO_MAP) {
        const normalizedKey = key.toLowerCase().replace(/[\\\/]/g, '/');

        // Проверяем разные варианты совпадений
        if (normalizedKey === normalizedSearchPath ||
            normalizedKey.includes(normalizedSearchPath) ||
            normalizedSearchPath.includes(normalizedKey)) {
            console.log(`✅ Найдено частичное совпадение: "${path}" -> "${key}"`);
            return AUDIO_MAP[key];
        }
    }

    // Вариант 4: Ищем по последней части пути
    const lastPart = path.split('/').pop() || path;
    for (const key in AUDIO_MAP) {
        if (key.includes(lastPart) || lastPart.includes(key.split('/').pop() || '')) {
            console.log(`✅ Найдено по последней части: "${path}" -> "${key}"`);
            return AUDIO_MAP[key];
        }
    }

    console.warn(`❌ Аудио не найдено для пути: ${path}`);
    console.warn(`🔍 Доступные ключи:`, Object.keys(AUDIO_MAP).slice(0, 10)); // Первые 10 для отладки

    return AUDIO_MAP['placeholder'];
};

/**
 * Получить объект для воспроизведения аудио
 */
export const getAudioSource = (path: string): any => {
    const audioId = getAudioAssetId(path);

    if (audioId !== null) {
        console.log(`✅ Получен аудио ID: ${audioId}`);
        return audioId;
    }

    console.error(`❌ Не удалось получить аудио ID для: ${path}`);
    return AUDIO_MAP['placeholder'];
};

